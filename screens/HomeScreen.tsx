import * as WebBrowser from "expo-web-browser";
import React, { useState, useEffect } from "react";
import {
  Image,
  Platform,
  ScrollView,
  StyleSheet,
  TouchableOpacity,
  View,
  AsyncStorage
} from "react-native";
import { Button, Text } from "react-native-paper";
import { MonoText } from "../components/StyledText";

export default function HomeScreen(props) {
  const [counter, setCounter] = useState(0);

  useEffect(() => {
    if (counter > 6 || counter < -6) setCounter(0);
  }, [counter]);

  const handleLogout = async () => {
    await AsyncStorage.removeItem("@AuthStore:authToken");
    props.navigation.navigate("Auth");
  };

  return (
    <View style={styles.container}>
      <ScrollView
        style={styles.container}
        contentContainerStyle={styles.contentContainer}
      >
        <View style={styles.welcomeContainer}>
          <Image
            source={
              __DEV__
                ? require("../assets/images/robot-dev.png")
                : require("../assets/images/robot-prod.png")
            }
            style={styles.welcomeImage}
          />
        </View>

        <View style={styles.getStartedContainer}>
          <DevelopmentModeNotice />

          <Text style={styles.getStartedText}>Get started by opening</Text>

          <View
            style={[styles.codeHighlightContainer, styles.homeScreenFilename]}
          >
            <MonoText>screens/HomeScreen.tsx</MonoText>
          </View>

          <View style={styles.helpContainer}>
            <TouchableOpacity onPress={handleHelpPress} style={styles.helpLink}>
              <Text style={styles.helpLinkText}>
                Help, it didnâ€™t automatically reload!
              </Text>
            </TouchableOpacity>
          </View>

          <Text style={styles.helpContainer}>
            Use Hooks in React Native Example
          </Text>
          <Text style={styles.getStartedText}>
            Counter(reset beyond +-6): {counter}
          </Text>

          <View style={{ flexDirection: "row" }}>
            <Button
              icon="add"
              mode="outlined"
              color="#1e88e5"
              onPress={() => setCounter(counter + 1)}
            >
              INC 1
            </Button>
            <Button
              icon="remove"
              mode="outlined"
              color="#f4511e"
              onPress={() => setCounter(counter - 1)}
            >
              DEC 1
            </Button>
          </View>

          <Text style={styles.helpContainer}>
            Go to Details page use card transitions
          </Text>

          <View>
            <Button
              mode="contained"
              onPress={() => {
                props.navigation.navigate("Details", {
                  someParam: "This is the param passed from HOME page.",
                  navType: "Card"
                });
              }}
            >
              Go to Details
            </Button>
          </View>

          <Text style={styles.helpContainer}>
            Delete authToken and navigate to LogIn screen
          </Text>
          <View>
            <Button mode="contained" onPress={handleLogout}>
              Log out
            </Button>
          </View>
        </View>
      </ScrollView>

      <View style={styles.tabBarInfoContainer}>
        <Text style={styles.tabBarInfoText}>
          This is a tab bar. You can edit it in:
        </Text>

        <View
          style={[styles.codeHighlightContainer, styles.navigationFilename]}
        >
          <MonoText style={styles.codeHighlightText}>
            navigation/MainTabNavigator.tsx
          </MonoText>
        </View>
      </View>
    </View>
  );
}

function DevelopmentModeNotice() {
  if (__DEV__) {
    const learnMoreButton = (
      <Text onPress={handleLearnMorePress} style={styles.helpLinkText}>
        Learn more
      </Text>
    );

    return (
      <Text style={styles.developmentModeText}>
        Development mode is enabled: your app will be slower but you can use
        useful development tools. {learnMoreButton}
      </Text>
    );
  } else {
    return (
      <Text style={styles.developmentModeText}>
        You are not in development mode: your app will run at full speed.
      </Text>
    );
  }
}

function handleLearnMorePress() {
  WebBrowser.openBrowserAsync(
    "https://docs.expo.io/versions/latest/workflow/development-mode/"
  );
}

function handleHelpPress() {
  WebBrowser.openBrowserAsync(
    "https://docs.expo.io/versions/latest/workflow/up-and-running/#cant-see-your-changes"
  );
}

